public class InfoLog {
    private String date;
    private String time;
    private String lvl;
    private String desc;
    private String method;
    private String exType;
    InfoLog(String date, String time, String lvl, String desc, String method, String exType){
        this.date = date;
        this.time = time;
        this.lvl = lvl;
        this.desc = desc;
        this.method = method;
        this.exType = exType;
    }
    }
